#include "libft.h"

int		ft_atoi(const char *str)
{
	long int	nb;
	int			sign;

	nb = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		sign = -1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + (*str) - 48;
		str++;
	}
	return ((int)(nb * sign));
}
