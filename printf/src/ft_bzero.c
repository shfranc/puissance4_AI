#include "minlib.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*uc_s;

	uc_s = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		uc_s[i] = '\0';
		i++;
	}
}
